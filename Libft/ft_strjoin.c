#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	s1_len;
	size_t	s2_len;

	CHECK_PTR(s1);
	CHECK_PTR(s2);
	s1_len = ft_strlen(s1);
	s2_len = ft_strlen(s2);
	CHECK_MALLOC(str, (s1_len + s2_len) + 1);
	ft_memcpy(str, s1, s1_len);
	ft_memcpy(str + s1_len, s2, s2_len);
	str[s1_len + s2_len] = '\0';
	return (str);
}
